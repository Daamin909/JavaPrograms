class repeatAsterick{
	public static void main(String[]args){
		int a;
		for (a=1; a<=1; a++)
		{
			System.out.println("*");
			System.out.println("**");
			System.out.println("***");
			System.out.println("****");
			System.out.println("*****");
			System.out.println("******");
			System.out.println("*******");
			System.out.println("********");
			System.out.println("*********");
			System.out.println("**********");
		}
	}
}

